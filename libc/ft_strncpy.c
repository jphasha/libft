char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;

	i = 0;
	if (src[i] != '\0' && (i < len))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] != '\0';
	return (dst);
}
